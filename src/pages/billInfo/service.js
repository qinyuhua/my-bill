export async function queryAllAmount(params) {
  if (!isWeiXin()) {
    return post('/billList/queryAllAmount', params);
  }
  const { startDate, endDate } = params;
  let totalPay = 0;
  let totalIncome = 0;

  const y = new Date().getFullYear();
  const m = addZero(new Date().getMonth() + 1);
  const d = addZero(new Date().getDate());
  const date = `${y}${m}${d}`;
  const allDataList = Taro.getStorageSync(date)

  allDataList.map(item => {
    if (new Date(item.date).getTime() >= new Date(startDate).getTime() && new Date(item.date).getTime() <= new Date(endDate).getTime()) {
      if (item.type === 'pay') {
        totalPay += parseFloat(item.amount);
      } else {
        totalIncome += parseFloat(item.amount)
      }
    }
  });

  return {
    "success":true,
    "data":{
      "totalPayAmount": totalPay,
      "totalIncomeAmount":totalIncome
    }
  };

}

