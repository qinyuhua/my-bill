export async function queryListGroup(params) {
  if (!isWeiXin()) {
    return post('/billList/queryListGroup', params);
  }
  const allBooksList = Taro.getStorageSync('allBooks') || [];
  const { startDate, endDate } = params;
  const y = new Date().getFullYear();
  const m = addZero(new Date().getMonth() + 1);
  const d = addZero(new Date().getDate());
  const date = `${y}${m}${d}`;
  const allDataList = Taro.getStorageSync(date)

  allDataList.sort((a, b) => a.date - b.date);

  let obj = [];
  allDataList.map(item => {

    if (new Date(item.date).getTime() >= new Date(startDate).getTime() && new Date(item.date).getTime() <= new Date(endDate).getTime()) {
      const books = allBooksList.find(aItem => aItem.billType === item.billType) || {};
      item = {...item, ...books};

      const curData = obj.find(oItem => oItem._id === item.date);
      if(curData) {
        curData.lists.push(item)
        if(item.type === 'pay') {
          curData.allPayAmount += item.amount;
        } else {
          curData.allIncomeAmout += item.amount;
        }
      } else {

        const o = {
          _id: item.date,
          lists: [{...item}],
          date: item.date,
          allPayAmount: item.type === 'pay' ? item.amount : 0,
          allIncomeAmout: item.type === 'income' ? item.amount : 0,
        };
        obj.push(o);
      }
    }
  });

  console.log(1113, obj)
  return {
    "success":true,
    "data": obj
  };


}

